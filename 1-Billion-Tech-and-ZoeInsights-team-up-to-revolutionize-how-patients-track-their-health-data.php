               1 Billion Tech, a software engineering services and technology product consulting company with offices in the USA and Sri Lanka, has teamed up with ZoeInsights to revolutionize the way patients track their health data.<br><br>

A medical condition diagnosis can be an extremely challenging time for patients and caregivers.  There is often a steep learning curve, where patients can feel overloaded and overwhelmed by all the information they are receiving, followed by periods of inactivity and limited communication.  The result can be physical and emotional exhaustion, and uncertainty over how to make the best decisions.<br><br>

To overcome this challenge, ZoeInsights, a digital health start-up based in Western Canada, has designed a platform that empowers patients and practitioners alike. The ZoeInsights application, launched in June 2020, provides a vital tool for patients and their loved ones to easily and securely manage, share and analyse complex health data. ZoeInsights is a mission-driven social enterprise empowering people with the resources, knowledge and tools they need to mobilize their own personalized health data, to connect meaningfully with the global health community, and to spend less time on healthcare and more time living their best possible lives.<br><br>

ZoeInsights was co-founded by Shaneel Pathak informed by his experience as a caregiver supporting his wife following a life-changing diagnosis of metastasic lung cancer. Shaneel understands the challenges patients and their caregivers face during treatment, including having limited tools to manage care, and support to make informed decisions. Since its launch, ZoeInsights has also received interest from hospitals and research organisations for a solution that helps organisations track the data of patients for research purposes, leading to the launch of ZoeDiscovery in September 2020.<br><br>

1 Billion Tech was approached by ZoeInsights because of its expertise in React Native development and the AWS Cloud. 1 Billion Tech provided ZoeInsights with professional software engineering services to develop and implement this innovative platform which helps patients manage their health journeys and researchers to support those patients. The team comprised of nearly 20 highly-skilled developers. Headquartered in Silicon Valley, 1 Billion Tech is an innovative software engineering services and technology product consulting company with global clients in multiple industry verticals.<br><br>

CEO and Cofounder of ZoeInsights, Shaneel Pathak stated, “The partnership with 1 Billion Tech started in 2019 and together we launched our SaaS health platform. They provided a full-service development team and managed multiple streams of work. The senior management fostered a trusted relationship and the team demonstrated commitment to deliver within our constraints. Foremost, the project management team provided their opinion on improving the project and identified gaps. I highly value their critical thinking.”<br><br>

Prasath Nanayakkara, Founder, Chairman and CEO of 1 Billion Tech stated, “This is an exciting project for the 1Billion Tech team and we are thankful to ZoeInsights for giving us the opportunity to be a part of this journey. Through this partnership, we have been able to harness the collective strengths of both companies to deliver a world-class product that is certainly changing lives for the better.”
