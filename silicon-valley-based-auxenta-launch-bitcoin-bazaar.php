                <p class="dropcap">Sri Lankan Technopreneurs Behind Silicon Valley based Auxenta Inc. Launch &lsquo;Bitcoin Bazaar&rsquo;</p>

                <p>Colombo, Sri Lanka – Sep 3, 2014 – Auxenta Inc., an innovative software and QA engineering services and technology

                  product consulting company headquartered in Silicon Valley with operations in Colombo, recently launched

                  a mobile marketplace app for buying and selling Bitcoins, the leading crypto-currency.</p>

                <p>Auxenta built the app named Bitcoin Bazaar for Bay Area startup Bay Coins, LLC. The app which is now available

                  for iOS and Android devices makes it the first fully functional app for matching buyers and sellers of

                  Bitcoin on both platforms. The app lets users find buyers and sellers in their neighborhood, using location

                  services from their mobile phone, and lets buyers and sellers transact using cash without needing to enter

                  bank or financial information into the app. Bitcoin Bazaar is available on iTunes App Store and Google

                  Play.

                </p>

                <p>Auxenta co founder and CTO Dr. Shahani Markus, who is also CTO of Bay Coins said, &ldquo;Mobile is a natural

                  fit for those who want to trade Bitcoins, and buying or selling with cash is still the easiest way to acquire

                  the currency. In the past, finding someone near you to transact with wasn&rsquo;t easy with mobile devices.

                  With Bitcoin Bazaar, anyone can buy or sell Bitcoin in person easily.

                  <br>

                  <br> Dr. Markus, a Computer Science Ph.D. from Purdue University, and former CTO of the ICT Agency of Sri Lanka,

                  helped launch Bay Coins alongside MIT alumnus and longtime colleague Prasath Nanayakkara. The duo also

                  co founded Auxenta in Sunnyvale, CA.</p>

