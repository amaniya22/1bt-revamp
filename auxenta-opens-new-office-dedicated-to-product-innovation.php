                <p class="mi_tx">

                  <strong>Auxenta Chairman, CEO & Founder Prasath Nanayakkara lightning the electronic oil lamp alongside Deputy

                    Minister of Foreign Affairs Dr. Harsha de Silva</strong>

                </p>

                <p class="dropcap"> Auxenta Inc, a Silicon Valley based IT company, recently moved to a new state-of-the-art office at 61, Level

                  1, Janadhipathi Mawatha, Colombo 1.



                  <p>The modern workspace was designed to create an exciting work environment for nurturing new ideas and is

                    dedicated to the company’s product innovations, one of which is to be launched soon – MillionSpaces.

                    The chief guest for the launch event was Deputy Minister of Foreign Affairs Dr. Harsha de Silva, who

                    commended Auxenta’s vision and strategy to ‘constantly innovate and up the ante’. Also in attendance

                    were SLASSCOM Vice Chairman Ruwindhu Peiris and Great Place to Work CEO Kshanika Ratnayaka.</p>



                  <p>The electronic oil lamp commemorating the opening of the office was lit in the presence of the minister,

                    special invitees, the leadership team in Colombo, and a virtual team comprising of a senior advisor/serial

                    entrepreneur in Silicon Valley, Riz Virk, and Auxenta’s Creative Director in New Zealand, Diresh Dodanduwa.</p>



                  <p>Commenting on the new office, Auxenta Founder, Chairman and CEO Prasath Nanayakkara said, “This new space

                    is a testament to our hard work and stellar performance that have enabled us to grow tremendously over

                    a short period of time. It will help us propel the company to greater heights and allow us to reflect,

                    learn and have fun while ensuring great service is delivered to our clients.”</p>



                  <p>Auxenta has drawn on the examples of other Silicon Valley tech companies that emphasise collaboration and

                    employee satisfaction to boost productivity. Designed for their product innovation teams, the new office

                    boasts an open floor plan with a breakout area, which facilitates employee interaction. Strategically

                    having these teams work in close proximity to each other will have a significant positive effect on their

                    ability to solve problems in real-time, directly impacting delivery excellence.</p>



                  <p>The event also commemorated the addition of the company’s 150th employee marking its continued fast-paced

                    growth in the industry. Highlighting this growth are the major milestones the company has achieved in

                    a period of three short years, which includes being the first company to develop a Bitcoin trading engine

                    for a Silicon Valley customer; being recognised as one of the 20 most promising companies in Quality

                    Management Systems for 2015 by US-based CIO Review magazine; awarded the ‘Best Startup of the Year 2016’

                    by the Computer Society of Sri Lanka; and bagging an international Innovation Award for their product

                    Assetminder.</p>



                  <p> Auxenta also used the occasion to unveil their new corporate values, which were designed in collaboration

                    with Great Place to Work, based on the importance of community, consistency, moral behaviour and learning.

                    They represent the importance Auxenta places on the human aspect of technology and will form the basis

                    of their unique approach to software engineering.</p>



                  <p>“These values have always been a part of our culture and now we just made it official. They are a reflection

                    of what we believe Auxenta should be and what everyone who works here should represent. We want to be

                    a positive force in the world and that must happen organically from the ground up,” said Auxenta COO

                    and VP of Quality Assurance Chandana Ranasinghe.</p>



                  <p>Ultimately these values and approach to business create a corporate culture of inclusivity that truly encourages

                    collaboration and enables innovation. The reason behind Auxenta’s meteoric rise is primarily due to the

                    attention they pay to their corporate culture for their ability to draw top talent. Hence in opening

                    the new office, Auxenta wanted to reinforce its culture and ensure it embodies the same ethos throughout

                    the company’s exponential growth.</p>

