                        Referred to as an ‘exceptional visionary’, Agrawal has in recent times held leadership roles at three successful startups: CEO of Aloqa, a mobile push platform (acquired by Motorola), VP Product and Marketing at Tellme Networks (acquired by Microsoft) and Founding CEO of Collegefeed (acquired by AfterCollege). Aloqa was featured by Google and Microsoft and Consumer Reports. It won awards from Mashable Top 10, VentureBeat Mobile, AlwaysOn Global 250, was the most shared App on Facebook in 2009 and declared Forbes Best Travel App in 2010. According to Auxenta Chairman and CEO Prasath Nanayakkara, “The company is enjoying a significant growth spurt and Sanjeev’s deep marketing and product strategy experience is exactly what we need to propel us into the next phase. I’m excited to be collaborating with a fellow MIT alumnus.” Sanjeev graduated Phi Beta Kappa with a BS and MS in Electrical Engineering/Computer Science from MIT and initially spent time at McKinsey &amp; Co. and Cisco Systems. He is a Forbes magazine contributor and also maintains a personal blog.

