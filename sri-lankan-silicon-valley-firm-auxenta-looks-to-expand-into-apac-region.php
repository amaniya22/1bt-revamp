                <p class="dropcap">Having its roots in Sri Lanka, the fast-growing boutique technology solutions firm Auxenta is currently poised

                  for dramatic expansion to the APAC region having bagged new clients in APAC countries Singapore and Australia.

                  Founded in 2014 in the global startup capital, Silicon Valley, Auxenta offers high value IT services complemented

                  by innovative software products. Within a short period of just over three years since it was set up, the

                  company is growing by leaps and bounds, posting an enviable year on year growth of 100% in the 2017 financial

                  year, while simultaneously expanding its headcount to 160 employees. Armed with valuable experience in

                  implementing diverse products ranging from BitCoin solutions to large enterprise billing and CRM software,

                  topped off with global delivery models for Fortune 1000 companies, the Auxenta team is perfectly poised

                  to partner clients to meet their strategies with innovative IT solutions.</p>



                <p>The Founder of Auxenta, Prasath Nanayakkara, says, “I am amazed by the pace at which Auxenta has grown in

                  such a short period. Our relatively recent entry in the tech sphere has given us the advantage of analysing

                  past success in the tech world, in order to come up with a winning formula which encompasses cutting edge

                  technology, a proficient engineering team and a brilliant technology centre in Colombo. Auxenta has already

                  entrenched its footprint in 7 countries across 4 continents and its strategic foray into the APAC region

                  will further strengthen Auxenta’s profile. We are bullish about the company’s prospects and boldly ambitious

                  about where we want to be positioned within the next couple of years.”</p>



                <p>Auxenta is active in US, UK, Ireland, New Zealand, Sri Lanka, Singapore and Australia across the continents

                  of Asia, Europe, Australia and America. The company’s tech solutions are already making potential clients

                  and envious rivals sit up and take note as it makes new product breakthroughs, which is a testament to

                  its unique approach to software design, a deep understanding of the engineering process and commitment

                  to quality. Assetminder, an ingenious fleet maintenance and workshop management solution developed by Auxenta

                  Inc. (Auxenta), won the Innovation Award at Ireland’s Fleet Transport Awards 2017 held in Dublin, Ireland,

                  recently.

                </p>



                <p>Perhaps the firm’s spanking new office space in Colombo offers the right mood for product innovation teams

                  to brainstorm amidst an open floor plan with a breakout area, which facilitates employee interaction. Strategically

                  having these teams work in close proximity to each other is having a significant positive effect on their

                  ability to solve problems in real-time, directly impacting delivery excellence.</p>



                <p>The efficacy of its workspace dynamic is affirmed by the tech firm’s Australian client Rizan Rizvi, Head

                  of Innovation, at Interrelate Ltd. “We are delighted to work with Auxenta. Interrelate is not just transforming

                  a 90-year business, we are standing in the future and redefining the industry as a whole. Auxenta is led

                  by experienced leadership blended with an energetic team that demonstrated the readiness to embrace ambiguity

                  and to give it a go instead of following conventional methods. We have engaged Auxenta to empower our clients

                  with choice of channels, such as omni channel, to engage in and to change the way we do business.”</p>



                <p>As the company expands further in to the APAC region, it acknowledges another client Camelot Trust, a boutique

                  corporate services company based in Singapore, for having placed their confidence in Auxenta, and as the

                  company’s portfolio grows, there has been no looking back.</p>



                <p>Hungry for challenging projects, the Auxenta team is proud of its newest product MillionSpaces, which leverages

                  mobile and cloud technologies for a unique experience. Here, guests can discover, pay and book unique spaces

                  online for parties, conferences, meetings, interviews, trainings, photoshoots, weddings, hot desks, sports

                  and concerts. The beta version of MillionSpaces will be launched soon. </p>

