                      design, deep understanding of the engineering process and commitment to quality. They also help showcase

                      the world class talent we have here in Sri Lanka.”</p>

    

                    <p> Assetminder is a cloud based fleet maintenance and workshop management software that is targeted at both

                      fleet managers and commercial vehicle workshops. It provides both web-based and mobile-based solutions,

                      and allows for both online and offline</p>

    

                    <p> “The Innovation Award category is always hotly contested and this year was no exception. However, Assetminder’s

                      ease of use and comprehensive features were widely praised and pushed us over the finish line. This was

                      in large part thanks to Auxenta’s experience and expertise in software development. It was their understanding

                      of our needs and grasp of the latest technology that produced such an innovative and successful software

                      platform,” said Assetminder Inc. Chief Technology Officer (CTO) Gerard Bennett.</p>

    

                    <p> Auxenta Inc. is an innovative software engineering services and technology product consulting company headquartered

                      in Silicon Valley with operations in Colombo. Established in Silicon Valley in 2014, Auxenta soon went

                      on to make tremendous progress in a short period of time acquiring 20 clients across the US, Europe and

                      Asia, and achieving a revenue growth of 300% in 2015. Auxenta has been awarded the Best Startup of the

                      Year 2016 by the Computer Society of Sri Lanka (CSSL).</p>

